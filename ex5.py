idade = int(input("Diga-me a tua idade para que possa atribuir uma categoria :  "))

if idade >5 and idade <=7:
     print("A tua categoria é a Infantil A ")
elif idade >8 and idade <=11:
     print("A tua categoria é a Infantil B ")
elif idade >12 and idade <=13:
     print("A tua categoria é a Juvenil A ")
elif idade >14 and idade <=17:
     print("A tua categoria é a Juvenil B ")
else:
     print("A tua categoria é a Adulto ")
