dia = int(input("digite um numero de 1 a 7"))

if dia == 1 :
    print("segunda-feira")
elif dia == 2 :
    print("Terça-feira")
elif dia == 3 :
    print("Quarta-feira")
elif dia == 4 :
    print ("Quinta-feira")
elif dia == 5 :
    print("Sexta-feira")
elif dia == 6 :
    print("Sábado")
else :
    print("Domigo")
